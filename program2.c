#include <stdio.h> // इनपुट/आउटपुट के लिए आवश्यक हेडर फाइल

int main() {
    int a, b, sum; // तीन integer वेरिएबल घोषित किए: a, b और sum

    // उपयोगकर्ता से दो नंबर इनपुट करने के लिए कहें
    printf("Enter two numbers: ");
    scanf("%d %d", &a, &b); // दो integer इनपुट को पढ़ें और a व b में स्टोर करें

    sum = a + b; // दोनों नंबरों को जोड़कर sum में स्टोर करें

    // योग को स्क्रीन पर प्रिंट करें
    printf("Sum = %d\n", sum);

    return 0; // प्रोग्राम सफलतापूर्वक समाप्त हुआ
}
